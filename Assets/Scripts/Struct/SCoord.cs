namespace T {

    public struct SCoord2 {

        public float X, Y;

        public SCoord2(float x, float y) {
            X = x;
            Y = y;
        }
    }

    public struct SCoord3 {

        public float X, Y, Z;

        public SCoord3(float x, float y, float z) {
            X = x;
            Y = y;
            Z = z;
        }
    }
}